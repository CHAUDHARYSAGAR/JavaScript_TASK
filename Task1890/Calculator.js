class Calculator {
    addition(value1, value2) {
        return value1 + value2;
    }
    subtraction(value1, value2) {
        return value1 - value2;
    }
    multiplication(value1, value2) {
        return value1 * value2;
    }
    division(value1, value2) {
        return value1 / value2;
    }
    modulus(value1, value2) {
        return value1 % value2;
    }
  }

  module.exports = Calculator;